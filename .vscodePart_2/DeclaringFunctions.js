// Function Declaration
function sum (x,y){
    return x + y
}
console.log(sum(1,2))

// function Express
const subtraction = function (x,y){
    return x - y
}
console.log(subtraction(1,2))

//named Function Express
const multiplication = function multiplication (x,y){
    return x * y
}
console.log(multiplication(1,2))